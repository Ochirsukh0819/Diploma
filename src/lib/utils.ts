export const calculateOverlapsByYear = (data: any) => {
  const countOverlapYear: any[] = [];
  const overlapsByYear: any = new Map<number, number>();

  data.forEach((item: any) => {
    const year = item.data.year;
    if (overlapsByYear.has(year)) {
      overlapsByYear.set(year, overlapsByYear.get(year) + 1);
    } else {
      overlapsByYear.set(year, 1);
    }
  });

  overlapsByYear.forEach((value: any, key: any) => {
    countOverlapYear.push([key, value]);
  });
  return countOverlapYear;
};

// Дүнгээр тооцоолох

export const calculateOverlapsByPoint = (data: any) => {
  const countOverlapPoint: any[] = [];
  const countMap = new Map();

  const updateCount = (countMap: any, region: any, value: any) => {
    const count = countMap.get(region) || 0;
    countMap.set(region, value ? count + 1 : count);
  };

  data.forEach((item: any) => {
    const { A, B, C, D } = calculateABCD(parseInt(item.data.rate));
    updateCount(countMap, "A", A);
    updateCount(countMap, "B", B);
    updateCount(countMap, "C", C);
    updateCount(countMap, "D", D);
  });
  countMap.forEach((value: any, key: any) => {
    countOverlapPoint.push([key, value]);
  });

  return countOverlapPoint;
};

export const calculateABCD = (point: any) => {
  const A = point <= 100 && point >= 90;
  const B = point <= 89 && point >= 80;
  const C = point >= 70 && point <= 79;
  const D = point >= 60 && point <= 69;
  return { A, B, C, D };
};

