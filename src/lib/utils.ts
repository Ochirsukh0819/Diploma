export const calculateAveragePoint = (filterData: any) => {
  const yearMap = new Map();

  filterData.forEach((item: any) => {
    const { year, rate } = item.data;
    if (yearMap.has(year)) {
      const current = yearMap.get(year);
      yearMap.set(year, {
        total: current.total + parseInt(rate),
        count: current.count + 1,
      });
    } else {
      yearMap.set(year, { total: parseInt(rate), count: 1 });
    }
  });

  const averageMap = new Map();

  yearMap.forEach((value, key) => {
    const average = value.total / value.count;
    averageMap.set(key, average);
  });

  return averageMap;
};

